def count_ways(nums, i, depth):

    if i == len(nums): return 0
    product = nums[i]
#    print("i: {0}, product: {1}".format(i, product))
    if depth == 0:
#        print("Got 1")
        return 1
    elif (i, depth) in memo:
#        print("Looking up: {0}".format(memo[(i, depth)]))
        return memo[(i, depth)]

    ways = 0
    for j in range(i + 1, len(nums)):
        mult = nums[j]
        if product % mult or mult > product:
            continue
        ways += count_ways(nums, j, depth - 1)

    ways += count_ways(nums, i + 1, depth)
    memo[(i, depth)] = ways
    return ways


